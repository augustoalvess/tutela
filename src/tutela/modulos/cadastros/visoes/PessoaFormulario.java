    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Pessoa pessoa = new Pessoa();
        pessoa.setIdPessoa(idPessoa);
        pessoa.setNome(nome.getText());
        pessoa.setEstadoCivil(estadoCivil.getSelectedItem().toString());
        pessoa.setDataNascimento(dataNascimento.getText());
        pessoa.setSexo((sexoM.isSelected()) ? Pessoa.sexoM : Pessoa.sexoF);
        pessoa.setOrigemEtnica(origemEtnica.getSelectedItem().toString());
        pessoa.setEstado(estado.getSelectedItem().toString());
        pessoa.setCidade(cidade.getText());
        pessoa.setBairro(bairro.getText());
        pessoa.setRua(rua.getText());
        pessoa.setNumero(numero.getText());
        pessoa.setComplemento(complemento.getText());
        pessoa.setRg(rg.getText());
        pessoa.setCpf(cpf.getText());
        pessoa.setTelefoneResidencial(telefoneResidencial.getText());
        pessoa.setTelefoneCelular(telefoneCelular.getText());
        pessoa.setEmail(email.getText());
        pessoa.seteConselheiro((conselheiroSim.isSelected()) ? true : false);

        PessoaDAO pessoaDao = new PessoaDAO();

        if ( pessoa.validaDadosObrigatorios() && pessoaDao.salvar(pessoa) )
        {
            String mensagem = (idPessoa > 0) ? "Registro atualizado com sucesso!" : "Registro efetuado com sucesso!";
            JOptionPane.showMessageDialog(null, mensagem, "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            pessoaBusca.atualizarTabela(pessoaDao.codigoSalvo);
            this.setVisible(false);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

