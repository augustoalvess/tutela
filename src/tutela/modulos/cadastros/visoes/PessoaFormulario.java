    public void populaForumlario(Pessoa pessoa)
    {
        idPessoa = pessoa.getIdPessoa();
        nome.setText(pessoa.getNome());
        estadoCivil.setSelectedItem(pessoa.getEstadoCivil());
        dataNascimento.setText(pessoa.getDataNascimento());
        
        if ( pessoa.getSexo().substring(0, 1).equals(Pessoa.sexoM) )
        {
            this.sexoMActionPerformed(null);
        }
        else
        {
            this.sexoFActionPerformed(null);
        }
        
        if ( pessoa.iseConselheiro() )
        {
            this.conselheiroSimActionPerformed(null);
        }
        else
        {
            this.conselheiroNaoActionPerformed(null);
        }
        
        origemEtnica.setSelectedItem(pessoa.getOrigemEtnica());
        estado.setSelectedItem(pessoa.getEstado());
        cidade.setText(pessoa.getCidade());
        bairro.setText(pessoa.getBairro());
        rua.setText(pessoa.getRua());
        numero.setText(pessoa.getNumero());
        complemento.setText(pessoa.getComplemento());
        cpf.setText(pessoa.getCpf());
        rg.setText(pessoa.getRg());
        telefoneResidencial.setText(pessoa.getTelefoneResidencial());
        telefoneCelular.setText(pessoa.getTelefoneCelular());
        email.setText(pessoa.getEmail());
    }

